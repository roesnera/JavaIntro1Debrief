package com.example.practice;


/*
    SHORTCUTS:
    main - public static void main(String[] args) {}
    sout - System.out.println()
    (windows) ctrl + j - shows a list of keyboard shortcuts
    (mac) cmd + j - shows a list of keyboard shortcuts
 */

public class Sout {
    public static void main(String[] args) {
        System.out.println("Hello, world!");
        // declare and define a variable of each of the 8 primitive data types in Java
        // (byte, short, int, long, float, double, boolean, char)
        byte myBalance = 10;
        short numOfRows = 40;
        int number = 90;
        long inches = 49L;
        float num = 1.9344F;
        double cricketScore = 6.0;
        boolean isActive = true;
        char capitalC = 'C';









        // use System.out.println() to display the values of each variable to the system
        System.out.println("This is byte data type:    " + myBalance);
        System.out.println("This is short data type:   " + numOfRows);
        System.out.println("This is int data type:     " + number);
        System.out.println("This is long data type:    " + inches);
        System.out.println("This is float data type:   " + num);
        System.out.println("This is double data type:  " + cricketScore);
        System.out.println("This is boolean data type: " + isActive);
        System.out.println("This is char data type:    " + capitalC);









    }
}
