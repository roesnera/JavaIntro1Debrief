package com.example.practice;

public class MathOps {
    public static void main(String[] args) {
        /*
            MATH OPERATIONS
            in this section, we will use basic math operations on number variables

            1. Create two variables of the same data type
            you may select any of the following data types:
            (int, float, double)

            2. then, using System.out.println(),
            print out the values resulting from performing each of the five basic math operations on them
            (+, -, *, /, %)
         */

            int balance1 = 6;
            int balance2 = 4;
            System.out.println("Add two variables: " + (balance1 + balance2));
            System.out.println("Subtract two variables: " + (balance1 - balance2));
            System.out.println("Multiply two variables: "+ (balance1 * balance2));
            System.out.println("Divide two variables: " + (balance1 / balance2));
            System.out.println("Modulo of two variables : " + (balance1 % balance2));










    }
}
